import UIKit

class ClipUpTableViewController: UITableViewController {

    var clipUpEvents: [ClipEvent]!
    
    func refreshClipUpEventsList(){
        // Get the master list of all clip events, and clear the current clip up list
        let allClipEvents = ClipEvent.downloadMasterArray()
        clipUpEvents=[]
        
        // check the master list for clip up events, and add them to the table
        for event in allClipEvents{
            if event.direction == .up {
                clipUpEvents.append(event)
            }
        }
        
        // reload the table
        self.tableView.reloadData()
    }
    
    override func viewDidLoad() {
        super.viewDidLoad()

        // Uncomment the following line to preserve selection between presentations
        // self.clearsSelectionOnViewWillAppear = false

        // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
        // self.navigationItem.rightBarButtonItem = self.editButtonItem
        refreshClipUpEventsList()
    }

    // MARK: - Table view data source

    override func numberOfSections(in tableView: UITableView) -> Int {
        // #warning Incomplete implementation, return the number of sections
        return 1
    }

    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        // #warning Incomplete implementation, return the number of rows
        return clipUpEvents.count
    }

    
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "ClipUpEvent", for: indexPath)

        let eventType = clipUpEvents[indexPath.row]
        
        cell.textLabel?.text = eventType.description
        cell.detailTextLabel?.text = "+ \(eventType.magnitude)"

        return cell
    }
    

